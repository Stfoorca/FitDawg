package com.example.fitdawg;

public class User {

    public String name;
    public String email;
    public String gender;
    public String year;

    public User(){

    }

    public User(String name, String email, String gender, String year){
        this.name = name;
        this.email = email;
        this.gender = gender;
        this.year = year;
    }
}
